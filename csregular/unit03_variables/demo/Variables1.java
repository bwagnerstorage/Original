public class Variables1
{
	public static void main(String[] args)
	{
		// variables
		int num1 = 0;				// 4 bytes
		double num2 = 0.0;			// 8 bytes

		System.out.println(num1 + " " + num2);

		num1 = 25;
		num2 = 5.75;

		System.out.println(num1 + " " + num2);


		num1 = 4.5;
		num2 = 6;

		System.out.println(num1 + " " + num2);
	}
}